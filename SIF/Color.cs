namespace SIF
{
    public class Color
    {
        public byte red;
        public byte green;
        public byte blue;
        public byte alpha;

        public Color(byte inred, byte ingreen, byte inblue, byte inalpha)
        {
            red = inred;
            green = ingreen;
            blue = inblue;
            alpha = inalpha;
        }
    }
}